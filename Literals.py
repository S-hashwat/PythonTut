# Literals are data assigned to variables or constants while programming

print(type(1_00_0000_000)) # Integer Data Type
print(type(1000000000)) # Integer Data Type
print(type(0o675)) # int octal number data type
print(type(1)) #boolean Data Type
print(type(45.50)) # Boolean Data Type
print(type("Hello")) # String Data Type
print(type('Hey')) # String Data Type

